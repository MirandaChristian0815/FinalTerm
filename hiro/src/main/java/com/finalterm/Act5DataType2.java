package com.finalterm;

public class Act5DataType2 {
    public static void main(String[] args) {
        
        //Primitive data type
        //1. byte data type
        byte cdm_a = 127;
        System.out.println("byte = " + cdm_a);

        //2. Short data type
        short cdm_b = 12340;
        System.out.println("Short = " + cdm_b);

        //3. int data type
        int cdm_c = 34824923;
        System.out.println("int = " + cdm_c);

        //4. Long data type 
        long CdmLong = 398209483248L;
        System.out.println("Long : " + CdmLong);

        //5. Float data type
        float CdmFloat = 3.3278978923f;
        System.out.println("Float = " + CdmFloat);

        //6. Double data type
        double Cdmdouble = 1.7544432525434234308;
        System.out.println("Double = " + Cdmdouble);

        //7. Char data type
        char Cdm = 'C';
        System.out.println("Char = " + Cdm);

        //8. Boolean data type
        Boolean BooleanChan = true;
        System.out.println("Boolean = " + BooleanChan);


    }
}
